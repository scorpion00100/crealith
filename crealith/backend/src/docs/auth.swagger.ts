/**
 * @swagger
 * /api/auth/register:
 *   post:
 *     tags: [Authentication]
 *     summary: Register a new user
 *     description: Create a new user account with email and password
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - email
 *               - password
 *               - firstName
 *               - lastName
 *             properties:
 *               email:
 *                 type: string
 *                 format: email
 *                 description: User email address
 *                 example: user@example.com
 *               password:
 *                 type: string
 *                 minLength: 8
 *                 description: User password (minimum 8 characters)
 *                 example: SecurePassword123!
 *               firstName:
 *                 type: string
 *                 description: User first name
 *                 example: John
 *               lastName:
 *                 type: string
 *                 description: User last name
 *                 example: Doe
 *               phone:
 *                 type: string
 *                 description: User phone number
 *                 example: +1234567890
 *     responses:
 *       201:
 *         description: User registered successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 user:
 *                   $ref: '#/components/schemas/User'
 *                 accessToken:
 *                   type: string
 *                   description: JWT access token
 *                 refreshToken:
 *                   type: string
 *                   description: JWT refresh token
 *       400:
 *         $ref: '#/components/responses/ValidationError'
 *       409:
 *         description: Email already exists
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 *       500:
 *         $ref: '#/components/responses/InternalServerError'
 */

/**
 * @swagger
 * /api/auth/google:
 *   get:
 *     tags: [Authentication]
 *     summary: Initier l'authentification Google OAuth2
 *     description: Redirige vers Google pour le consentement. Paramètre `redirect` optionnel pour la redirection finale.
 *     parameters:
 *       - in: query
 *         name: redirect
 *         schema:
 *           type: string
 *         description: URL de redirection finale autorisée (doit correspondre à FRONTEND_URL)
 *     responses:
 *       302:
 *         description: Redirection vers Google OAuth2
 *       400:
 *         $ref: '#/components/responses/ValidationError'
 */

/**
 * @swagger
 * /api/auth/google/callback:
 *   get:
 *     tags: [Authentication]
 *     summary: Callback de Google OAuth2
 *     description: Après le consentement, Google redirige ici. Le backend crée/lie l'utilisateur, émet des JWT et redirige vers le frontend avec les tokens en fragment d'URL.
 *     responses:
 *       302:
 *         description: Redirection frontend avec `#accessToken` et `#refreshToken`
 *       401:
 *         $ref: '#/components/responses/UnauthorizedError'
 *       500:
 *         $ref: '#/components/responses/InternalServerError'
 */

